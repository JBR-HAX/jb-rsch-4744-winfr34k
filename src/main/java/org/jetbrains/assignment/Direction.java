package org.jetbrains.assignment;

/**
 * Walking direction of a robot.
 */
public enum Direction {
    /**
     * Positive movement on the X (vertical) axis.
     */
    NORTH,

    /**
     * Positive movement on the Y (horizontal) axis.
     */
    EAST,

    /**
     * Negative movement on the X (vertical) axis.
     */
    SOUTH,

    /**
     * Negative movement on the Y (horizontal) axis.
     */
    WEST
}
